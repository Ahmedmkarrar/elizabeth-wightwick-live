async function seed() {
  console.log(`\nSeeding ${properties.length} properties into Supabase...\n`)

  // Optional: clear existing properties first
  // const { error: deleteError } = await supabase.from('properties').delete().neq('id', '00000000-0000-0000-0000-000000000000')
  // if (deleteError) { console.error('Error clearing:', deleteError.message); process.exit(1) }

  const batchSize = 10
  let inserted = 0

  for (let i = 0; i < properties.length; i += batchSize) {
    const batch = properties.slice(i, i + batchSize)
    const { data, error } = await supabase.from('properties').insert(batch).select('id, title')

    if (error) {
      console.error(`❌ Error on batch ${Math.floor(i / batchSize) + 1}:`, error.message)
      process.exit(1)
    }

    inserted += data.length
    data.forEach(p => console.log(`  ✓ ${p.title}`))
  }

  console.log(`\n✅ Successfully seeded ${inserted} properties.\n`)
}

